package Tugas9;

public class Array {
		 public static void main(String[] args) {

		        // membuat array
		        int angka[] = {6,1,90,33,28};

		        // menggunakan perulangan For each untuk menampilkan angka
		        for( int x : angka ){
		            System.out.print(x + " ");
		        }

		    }
	}
